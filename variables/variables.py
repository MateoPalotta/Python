#variable definida
nombre = "Mateo"

#concatenar con f-strings
hola = f"Como estas? {nombre}"

#concatenar con +
hola = f"Como estas? " + nombre

#operadores de pertenencia para saber si algo esta o no esta en una variable es (in / not in)

print("Mateo" in hola) #verdadero / true
print("Mateo" not in hola) #falso / false